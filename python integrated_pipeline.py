    if pd.isna(s) or s == "":
        return []
    chars = list(str(s))
    days_map = {"일":"Sun","월":"Mon","화":"Tue","수":"Wed","목":"Thu","금":"Fri","토":"Sat"}
    result = []
    buf = ""
    for ch in chars:
        if ch in days_map:
            result.append(days_map[ch])
    if len(result)==0:
        tokens = [t.strip() for t in str(s).replace(","," ").split()]
        for tok in tokens:
            if tok in days_map: result.append(days_map[tok])
    seen=set(); ordered=[]
    for d in result:
        if d not in seen:
            seen.add(d); ordered.append(d)
    return ordered

