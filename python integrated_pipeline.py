# --- Problem data ------------------------------------------------------------
commodities = ['General', 'Coal', 'Container']
demand_stats = {
    'General': {'mean': 1.5e7, 'std': 1.8e6},
    'Coal': {'mean': 3.5e6, 'std': 0.5e6},
    'Container': {'mean': 2.5e6, 'std': 0.4e6}
}
revenues = {'General': 60, 'Coal': 40, 'Container': 80}
penalties = {c: revenues[c] * 1.5 for c in commodities}
train_capacity = {'General': 3000, 'Coal': 3500, 'Container': 1000}
emissions_per_train = {'General': 8.0, 'Coal': 9.5, 'Container': 4.5}
total_trains = 148

# Scenario generation
np.random.seed(42)
n_scenarios = 50
scenarios = {
    c: np.clip(
        np.random.normal(demand_stats[c]['mean'], demand_stats[c]['std'], n_scenarios),
        0,
        None
    )
    for c in commodities
}
scenario_prob = 1.0 / n_scenarios

# Deterministic benchmark (already used later in Figure 9)
det_demand = {c: demand_stats[c]['mean'] for c in commodities}
det_trains_per_ton = {c: 1 / train_capacity[c] for c in commodities}
det_trains_needed = sum(det_demand[c] * det_trains_per_ton[c] for c in commodities)
det_alloc = {'General': 133, 'Coal': 15, 'Container': 0}
det_profit_exp = sum(det_alloc[c] * train_capacity[c] * revenues[c] for c in commodities) / 1e9
det_emissions = sum(det_alloc[c] * emissions_per_train[c] for c in commodities)

# --- Helper functions --------------------------------------------------------
def compute_metrics(train_alloc: dict):
    """Evaluate allocation performance over Monte Carlo scenarios."""
    expected_profit = 0.0
    service_levels = []
    for s in range(n_scenarios):
        total_demand = 0.0
        total_shortage = 0.0
        for c in commodities:
            demand = scenarios[c][s]
            capacity = train_capacity[c] * train_alloc[c]
            served = min(capacity, demand)
            shortage = demand - served
            expected_profit += scenario_prob * (revenues[c] * served - penalties[c] * shortage)
            total_demand += demand
            total_shortage += shortage
        if total_demand > 0:
            service_levels.append(1 - total_shortage / total_demand)
        else:
            service_levels.append(1.0)
    emissions = sum(train_alloc[c] * emissions_per_train[c] for c in commodities)
    return {
        "profit_billion": expected_profit / 1e9,
        "service_levels": service_levels,
        "emissions": emissions
    }

def greedy_allocation():
    """Heuristic fallback: proportional allocation based on expected train needs."""
    fractional_needs = {c: det_demand[c] / train_capacity[c] for c in commodities}
    alloc = {c: int(np.floor(fractional_needs[c])) for c in commodities}
    remaining = total_trains - sum(alloc.values())

    if remaining > 0:
        order = sorted(commodities, key=lambda c: fractional_needs[c] - alloc[c], reverse=True)
        idx = 0
        while remaining > 0:
            alloc[order[idx % len(order)]] += 1
            remaining -= 1
            idx += 1
    elif remaining < 0:
        order = sorted(commodities, key=lambda c: alloc[c] - fractional_needs[c], reverse=True)
        idx = 0
        while remaining < 0:
            alloc[order[idx % len(order)]] -= 1
            remaining += 1
            idx += 1

    return alloc

def run_pyomo_solver():
    """Solve the stochastic allocation with Pyomo if possible."""
    model = ConcreteModel()
    model.COMMODITIES = Set(initialize=commodities)
    model.SCENARIOS = Set(initialize=range(n_scenarios))

    model.x = Var(model.COMMODITIES, within=NonNegativeIntegers)
    model.z = Var(model.COMMODITIES, model.SCENARIOS, within=NonNegativeReals)
    model.u = Var(model.COMMODITIES, model.SCENARIOS, within=NonNegativeReals)

    def objective_rule(m):
        return sum(
            scenario_prob * (revenues[c] * m.z[c, s] - penalties[c] * m.u[c, s])
            for c in m.COMMODITIES for s in m.SCENARIOS
        )
    model.profit = Objective(rule=objective_rule, sense=maximize)

    model.total_trains_con = Constraint(expr=sum(model.x[c] for c in model.COMMODITIES) <= total_trains)

    def demand_fulfillment_rule(m, c, s):
        return m.z[c, s] + m.u[c, s] == scenarios[c][s]
    model.demand_con = Constraint(model.COMMODITIES, model.SCENARIOS, rule=demand_fulfillment_rule)

    def capacity_rule(m, c, s):
        return m.z[c, s] <= train_capacity[c] * m.x[c]
    model.capacity_con = Constraint(model.COMMODITIES, model.SCENARIOS, rule=capacity_rule)

    solvers = ['highs', 'glpk', 'cbc', 'ipopt']
    solver_name = None
    for cand in solvers:
        try:
            solver = SolverFactory(cand)
            if solver.available():
                solver_name = cand
                break
        except Exception:
            continue

    if solver_name is None:
        raise RuntimeError("No compatible MILP/NLP solver is available for Pyomo.")

    print(f"  Using {solver_name} solver...")
    solver = SolverFactory(solver_name)
    results = solver.solve(model, tee=False)
    if (hasattr(results, 'solver') and
            results.solver.termination_condition != TerminationCondition.optimal):
        raise RuntimeError(f"Solver did not reach optimality (status: {results.solver.status}).")

    alloc = {c: round(model.x[c].value) for c in commodities}
    profit_billion = value(model.profit) / 1e9
    service_levels = []
    for s in model.SCENARIOS:
        total_demand = sum(scenarios[c][s] for c in commodities)
        total_shortage = sum(value(model.u[c, s]) for c in commodities)
        level = 1 - total_shortage / total_demand if total_demand > 0 else 1.0
        service_levels.append(level)
    emissions = sum(alloc[c] * emissions_per_train[c] for c in commodities)

    return {
        "mode": "pyomo",
        "alloc": alloc,
        "profit_billion": profit_billion,
        "service_levels": service_levels,
        "emissions": emissions,
        "notes": f"Pyomo + {solver_name}"
    }

